#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	if (ac >= 2)
	{
		j = 1;
		while (ac - j >= 1)
		{
			i = 0;
			while (av[ac - j][i])
			{
				write(1, &av[ac - j][i], 1);
				i++;
			}
			write(1, "\n", 1);
			j++;
		}
	}
}
