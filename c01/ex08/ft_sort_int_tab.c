void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	tem;

	i = 0;
	while (i < size - 1)
	{
		j = i + 1;
		while (j < size)
		{
			if (tab[i] > tab[j])
			{
				tem = tab[i];
				tab[i] = tab[j];
				tab[j] = tem;
			}
			j++;
		}
		i++;
	}
}
